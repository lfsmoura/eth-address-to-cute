let words = getWords();

function mapToWord(hexStr, wordList) {
  const index = parseInt(hexStr, 16) % wordList.length;
  return wordList[index];
}

function encodeHexToEmoji(hexString) {
  let emojiString = "";
  // Starting Unicode code point for emojis is 0x1F600 (smileys)
  // You can change this range depending on which emojis you want to use
  const startEmojiCodePoint = 0x1f600;
  for (let i = 0; i < hexString.length; i += 4) {
    // Taking 4 characters at a time
    const hexChunk = hexString.substr(i, 4);
    const value = parseInt(hexChunk, 16);
    // Map the hex value to a specific emoji code point
    const emojiCodePoint = startEmojiCodePoint + (value % 80); // Limiting to 80 different emojis for simplicity
    emojiString += String.fromCodePoint(emojiCodePoint);
  }
  return emojiString;
}

const regex = /0x[a-fA-F0-9]{40}/g;

function generateUniqueName(ethereumAddress) {
  try {
    const n = 4; // Number of characters to use for word mapping
    const withoutPrefix = ethereumAddress.substring(2); // Remove "0x"

    const firstWordHex = withoutPrefix.substring(0, n);
    const lastWordHex = withoutPrefix.substring(withoutPrefix.length - n);

    const firstWord = mapToWord(firstWordHex, words);
    const lastWord = mapToWord(lastWordHex, words);

    const remainingHex = withoutPrefix.substring(n, withoutPrefix.length - n);

    return `${firstWord} ${lastWord}#${encodeHexToEmoji(remainingHex)}`;
  } catch (e) {
    console.error(ethereumAddress, e);
    return ethereumAddress;
  }
}

function walkDOM(rootNode) {
  let node = rootNode.firstChild;
  while (node) {
    if (node.nodeType === 3) {
      // Text node
      let text = node.data;
      let matches = text.match(regex);
      if (matches) {
        matches.forEach((match) => {
          const newName = generateUniqueName(match);
          text = text.replace(match, newName);
        });
        node.data = text;
      }
    } else {
      walkDOM(node);
    }
    node = node.nextSibling;
  }
}

